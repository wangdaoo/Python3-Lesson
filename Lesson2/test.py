# 变量与字符串
a = 1
b = 2
print(a + b)

print(a * b)

print(a / b)

print(a - b)

s1 = 'mars'
s2 = 'wong'

print(s1, s2)

# 字符函数
msg = 'HELLO world!'

print(msg)
print(msg.title())
print(msg.upper())
print(msg.lower())

# 转义字符
msg = 'I can: \n\tPython\n\tJavaScript'
print(msg)

# 删除空白
massage = '  Mars is NB   '
print('|' + massage + '|')
print('|' + massage.rstrip() + '|')
print('|' + massage.lstrip() + '|')
print('|' + massage.strip() + '|')
